/**
 * Inline SVG icons — replaces lucide-react dependency.
 * All icons are 24x24 stroke-based SVGs matching lucide's API:
 *   <IconName className="..." style={{}} strokeWidth={n} />
 */
import React from 'react';

interface IconProps extends React.SVGProps<SVGSVGElement> {
  size?: number | string;
  strokeWidth?: number;
}

function icon(paths: string, displayName: string) {
  const Icon = React.forwardRef<SVGSVGElement, IconProps>(
    ({ size = 24, strokeWidth = 2, className, style, ...props }, ref) => (
      <svg
        ref={ref}
        xmlns="http://www.w3.org/2000/svg"
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth={strokeWidth}
        strokeLinecap="round"
        strokeLinejoin="round"
        className={className}
        style={style}
        {...props}
      >
        <g dangerouslySetInnerHTML={{ __html: paths }} />
      </svg>
    )
  );
  Icon.displayName = displayName;
  return Icon;
}

// Special icon with fill support
function iconWithFill(paths: string, displayName: string) {
  const Icon = React.forwardRef<SVGSVGElement, IconProps & { fill?: string }>(
    ({ size = 24, strokeWidth = 2, fill = 'none', className, style, ...props }, ref) => (
      <svg
        ref={ref}
        xmlns="http://www.w3.org/2000/svg"
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill={fill}
        stroke="currentColor"
        strokeWidth={strokeWidth}
        strokeLinecap="round"
        strokeLinejoin="round"
        className={className}
        style={style}
        {...props}
      >
        <g dangerouslySetInnerHTML={{ __html: paths }} />
      </svg>
    )
  );
  Icon.displayName = displayName;
  return Icon;
}

